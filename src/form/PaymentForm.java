    private void prosesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_prosesButtonActionPerformed
        if(TotalPriceTxt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "pilihan belum lengkap");
            }else if(DpTxt.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "DP belum di isi");
            }else if(InstalAmounTxt.getText().isEmpty()){
                JOptionPane.showMessageDialog(null, "Isi jumblah yang di inginkan");
            }else{
                long landPrice = Long.parseLong(PriceLandTxt.getText()), buildingPrice = Long.parseLong(BuildingPriceTxt.getText()), availableLand = Long.parseLong(AvailableLandTxt.getText()), DP = Long.parseLong(DpTxt.getText()), instalAmoun = Long.parseLong(InstalAmounTxt.getText()), totalPrice, PPN, InstalPerMounth;
                totalPrice = (availableLand*landPrice) + buildingPrice;
                PPN = totalPrice*10/100;
                InstalPerMounth = (totalPrice + PPN) /instalAmoun;
                PpnTxt.setText(""+PPN);
                InstalmentMounTxt.setText(""+InstalPerMounth);
            }
                
    }//GEN-LAST:event_prosesButtonActionPerformed

