    private void simpanButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanButtonActionPerformed
        if(AggreeCek.isSelected() == false) {
            JOptionPane.showMessageDialog(null, "klik kondirmasi centangbox", "ERROR", JOptionPane.ERROR_MESSAGE);
       } else {
            String typeHouse;
            if (T36.isSelected()){
                typeHouse = "TIPE 36";                                   
            }else if(T45.isSelected()){
                typeHouse = "TIPE 45";
            }else {
                typeHouse = "TIPE 56";
            }
            
            try{
                String sql = "INSERT INTO rumah(Nama,Area,Tipe,Luas,Harga,Jumblah_Cicilan,Cicilan_bulan) VALUES ('"
                        + OrderNameTxt.getText() + "','"  
                        + AreaCombo.getSelectedItem() + "','"
                        + typeHouse + "','"
                        + LargeLandTxt.getText()+ "','"
                        + TotalPriceTxt.getText()+ "','"
                        + InstalAmounTxt.getText()+ "','"
                        + InstalmentMounTxt.getText()+"')";
            Connection conn = (Connection)koneksi.connectDatabse();
                PreparableStatement pst = (PreparableStatement) conn.prepareStatement(sql);
            pst.execute();
            } catch (SQLException ex) {
                Logger.getLogger(PaymentForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            OptionMenu OptionMenu = new OptionMenu();
            OptionMenu.setVisible(true);
            this.getDesktopPane().add(OptionMenu);
            this.dispose();
        }
    }//GEN-LAST:event_simpanButtonActionPerformed

   

